    /* ============ Edit Button ============= */ 
    $scope.edit = function (id) {
        document.getElementById("TEXT_FY_YEAR_CD").focus();

        $scope.temp = {
            hmid:id.HMID,
            ddlLocation:(id.LOCID).toString(),
            TEXT_FY_YEAR_CD: id.FY_YEAR_CD,
            txtDate: new Date(id.HDATE),
            txtOccassion: id.HOCCASSION,
            txtRemark: id.REMARKS,
            TEXT_SCHOOL_ID: (id.SCHOOL_ID).toString(),
        };

        $scope.editMode = true;
        $scope.index = $scope.post.getHolidays.indexOf(id);
    }
    
    
    /* ============ Clear Form =========== */ 
    $scope.clear = function(){
        document.getElementById("TEXT_SCHOOL_ID").focus();
        $scope.temp={};
        $scope.editMode = false;

        $scope.temp.txtDate = new Date();
        
    }


    /* ========== DELETE =========== */
    $scope.delete = function (id) {
        var r = confirm("Are you sure want to delete this record!");
        if (r == true) {
            $http({
                method: 'post',
                url: url,
                data: $.param({ 'hmid': id.HMID, 'type': 'delete' }),
                headers: { 'Content-Type': 'application/x-www-form-urlencoded' }
            }).
		    then(function (data, status, headers, config) {
                // console.log(data.data)
		        if (data.data.success) {
		            var index = $scope.post.getHolidays.indexOf(id);
		            $scope.post.getHolidays.splice(index, 1);
                    $scope.clear();
                    
		            $scope.messageSuccess(data.data.message);
		        } else {
		            $scope.messageFailure(data.data.message);
		        }
		    })
        }
    }
    


